export function findCountry(name) {
  if (!name) return null;
  const q = name.trim().toLowerCase();
  return COUNTRIES.find((c) => c.name.toLowerCase() === q) ?? null;
}

export function filterCountries(query, limit = 60) {
  if (!query) return COUNTRIES.slice(0, limit);
  const q = query.trim().toLowerCase();
  if (!q) return COUNTRIES.slice(0, limit);

  const starts = [];
  const contains = [];
  for (const c of COUNTRIES) {
    const name = c.name.toLowerCase();
    if (name.startsWith(q)) starts.push(c);
    else if (name.includes(q)) contains.push(c);
    if (starts.length + contains.length >= limit) break;
  }
  return [...starts, ...contains].slice(0, limit);
}
